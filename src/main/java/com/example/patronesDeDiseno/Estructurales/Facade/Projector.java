public class Projector {
    public void turnOn() {
        System.out.println("Projector is on");
    }

    public void turnOff() {
        System.out.println("Projector is off");
    }

    public void play() {
        System.out.println("Projector is playing movie");
    }

    public void stop() {
        System.out.println("Projector stopped");
    }
}
